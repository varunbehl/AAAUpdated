package com.aaa.rspweb.pages;

import org.openqa.selenium.By;

public class RSPLoginPage {

    //lookout manageuser and order details arrived for remaining locators
    public static By txtUserName;
    public static By txtPassword;
    public static By btnSignIn;
    public static By lnkChangePassword;
    public static By lnkForgotPassword;
    public static By lblLinks;
    public static By cboxRememberMe;
    public static By lblVersion;
    static {
        txtUserName = By.id("username-input");
        txtPassword = By.xpath("//input[@type='password']");
        btnSignIn = By.xpath("//button[@class='btn btn-primary']");
        lnkChangePassword = By.xpath("//button[@class='btn btn-link']");
        lnkForgotPassword = By.xpath("//a[text()='Forgot Password?']");
        lblLinks = By.xpath("//span[text()='Links']");
        cboxRememberMe = By.xpath("//input[@type='checkbox']");
        lblVersion = By.xpath("//div[@class='text-right'][1]");
    }
}
